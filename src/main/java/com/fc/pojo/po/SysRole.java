public class SysRole {
    /**
     * 主键
     */
    private Short id;
    /**
     * 角色名称
     */
    private String name;


    public Short getId() {
        return id;
    }

    public void setId(Short id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}


